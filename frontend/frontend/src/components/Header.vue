        sendDataLogin() {
            if (this.username === "") {
                var element = document.querySelector("#username");
                element.classList.replace("border-gray-300", "border-red-500")
                return
            } else {
                var element = document.querySelector("#username");
                element.classList.replace("border-red-500", "border-gray-300")
            }

            if (this.password === "") {
                var element = document.querySelector("#password");
                element.classList.replace("border-gray-300", "border-red-500")
                return
            } else {
                var element = document.querySelector("#password");
                element.classList.replace("border-red-500", "border-gray-300")
            }
            axios
                .post("http://127.0.0.1:8000/api/user/login", {"username": this.username, "password": this.password})
                .then((response) => {
                    if (response.data.success === true) {
                        localStorage.setItem("token", response.data.token);
                        const $targetEl = document.getElementById('authentication-modal');
                        const modal = new Modal($targetEl);
                        modal.hide();
                        window.location.reload();
                    }
                }) 
                .catch((error) => {
                    this.auth = error.response.data.success;
                })
        },
        sendDataRegister() {
            alert(this.username + " " + this.email + " " + this.password)
        },
        
        checkAuthenticated() {
            if (localStorage.getItem("token") === null) {
                return false
            } else {
                return true
            }
        },
